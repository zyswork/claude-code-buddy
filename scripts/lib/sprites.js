const HAT_LINES = {
  none: '',
  crown: '   \\^^^/    ',
  tophat: '   [___]    ',
  propeller: '    -+-     ',
  halo: '   (   )    ',
  wizard: '    /^\\     ',
  beanie: '   (___)    ',
  tinyduck: '    ,>      ',
};

function renderSprite(bones, frame = 0) {
  const frames = BODIES[bones.species];
  const idx = frame % frames.length;
  const body = frames[idx].map((line) => line.replaceAll('{E}', bones.eye));
  const lines = [...body];
  // 只在第 0 行是空行时替换成帽子（某些抖动帧会占用第 0 行画小星星等）
  if (bones.hat !== 'none' && !lines[0].trim()) {
    lines[0] = HAT_LINES[bones.hat];
  }
  // 如果完全没有帽子，且所有帧的第 0 行都是空的，去掉这一行避免浪费空间
  if (!lines[0].trim() && frames.every((f) => !f[0].trim())) lines.shift();
  return lines;
}

function spriteFrameCount(species) {
  return BODIES[species].length;
}

// 窄终端 / statusline 用的一行表情
function renderFace(bones) {
  const e = bones.eye;
  switch (bones.species) {
    case 'duck':
    case 'goose':    return `(${e}>`;
    case 'blob':     return `(${e}${e})`;
    case 'cat':      return `=${e}ω${e}=`;
    case 'dragon':   return `<${e}~${e}>`;
    case 'octopus':  return `~(${e}${e})~`;
    case 'owl':      return `(${e})(${e})`;
    case 'penguin':  return `(${e}>)`;
    case 'turtle':   return `[${e}_${e}]`;
    case 'snail':    return `${e}(@)`;
    case 'ghost':    return `/${e}${e}\\`;
    case 'axolotl':  return `}${e}.${e}{`;
    case 'capybara': return `(${e}oo${e})`;
    case 'cactus':   return `|${e}  ${e}|`;
    case 'robot':    return `[${e}${e}]`;
    case 'rabbit':   return `(${e}..${e})`;
    case 'mushroom': return `|${e}  ${e}|`;
    case 'chonk':    return `(${e}.${e})`;
    case 'zhulong':  return `<${e}>~~`;
    case 'yinglong': return `<${e}~${e}>`;
    case 'jingwei':  return `(${e}>o`;
    case 'taotie':   return `/${e}V${e}\\`;
    case 'daji':     return `(${e}w${e})`;
    default:         return `(${e}${e})`;
  }
}

module.exports = { renderSprite, renderFace, spriteFrameCount, HAT_LINES };
